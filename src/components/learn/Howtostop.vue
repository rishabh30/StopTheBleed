<h3>Compress and Control</h3>

<h4>Key Point</h4>
<p>There are a number of methods that can be used to stop bleeding 
    and they all have one thing in common—compressing a bleeding blood vessel in order 
    to stop the bleeding.&nbsp;</p>

<h4>If you don’t have a trauma first aid kit:</h4>
<p>
Apply direct pressure on the wound (Cover the wound with a clean cloth and apply pressure by pushing directly on it with both hands)
</p>
<ol>
    <li>Take any clean cloth (for example, a shirt) and cover the wound.</li>
    <li>If the wound is large and deep, try to “stuff” the cloth down into the wound.</li>
    <li>Apply continuous pressure with both hands directly on top of the bleeding wound.</li>
    <li>Push down as hard as you can.</li>
    <li>Hold pressure to stop bleeding. Continue pressure until relieved by medical responders.</li>
</ol>

<h4>If you do have a trauma first aid kit:</h4>
<p>For life-threatening bleeding from an arm or leg and a tourniquet is NOT available OR for bleeding from the neck, shoulder or groin:</p>
<ul>
    <li>Pack (stuff) the wound with a bleeding control (also called a hemostatic) gauze, plain gauze, or a clean cloth and then apply pressure with both hands </li>
</ul>
<ol>
    <li>Open the clothing over the bleeding wound. (A)</li>
    <li>Wipe away any pooled blood.</li>
    <li>Pack (stuff) the wound with bleeding control gauze (preferred), plain gauze, or clean cloth. (B)</li>
    <li>Apply steady pressure with both hands directly on top of the bleeding wound. (C)</li>
    <li>Push down as hard as you can.</li>
    <li>Hold pressure to stop bleeding. Continue pressure until relieved by medical responders. </li>
</ol>

<p>For life-threatening bleeding from an arm or leg and a tourniquet is available:</p>
<ul>
    <li>Apply the tourniquet </li>
</ul>
<ol>
    <li>Wrap the tourniquet around the bleeding arm or leg about 2 to 3 inches above the bleeding site (be sure NOT to place the tourniquet onto a joint—go above the joint if necessary).</li>
    <li>Pull the free end of the tourniquet to make it as tight as possible and secure the free end. (A)</li>
    <li>Twist or wind the windlass until bleeding stops. (B)</li>
    <li>Secure the windlass to keep the tourniquet tight. (C)</li>
    <li>Note the time the tourniquet was applied. (D)</li>
</ol>
